		WebUI.openBrowser('')
	}

	@When("the user navigates to the google home page")
	public void the_user_navigates_to_the_google_home_page() {
		WebUI.navigateToUrl('https://www.google.com/')		
		WebUI.click(findTestObject('Object Repository/Page_Google/div_Aceito'))	
	}

	@Then("the user sees an empty textbox")
	public void the_user_sees_an_empty_textbox() {	
		WebUI.verifyElementText(findTestObject('Object Repository/Page_Google/input__q'), '')		
		WebUI.closeBrowser()
	}
